	<h4> Serve this with rice... </h4>
	<br>
	 <h4>Ingredients:</h4>
	 <ul>
	     <li>4 dl Rice </li>
	     <li>Oil </li>
		 <li>1 tablespoon Curry </li>
		 <li>8 dl water </li>
		 <li>1 Garlic clove (pressed) </li>
		 <li> Zest from 1/2 lemon </li>
		 <li> Small piece Cinnamon stick </li>
	</ul>
	<h4>Directions:</h4>
	<h5> Sweet sour Chicken: </h5> 
	<ol> 
	     <li> Fry the oniens in a few tablespoons of oil. </li>
		<li> After the oniens are soft put in the garlic, cinnamon stick 
		 and cloves. </li>
		<li> Put in the chicken to fry. </li>
		 <li>When the chicken is starting to cook, put in the 
		 veggie stock dice and a pinch of salt and pepper.</li> 
		<li> Let that cook a little bit </li>
		<li> Put in the diced tomatoes. Let that cook for a few minutes. </li>
		<li> Then add in the chilli sause, sweet chilli sause,<br> 
		 paprica powder, curry, honey, lemon juice.</li>

	</ol>
	
	<h5> Curry rice: </h5> 
	   <ol> 
		 <li> Fry the garlic and cinnamon stick in some oil. </li>
		 <li>Put in the rice, lemon zest and curry then add water. </li>
		 <li>Let it cook on low heat with a lid on. </li>
	   </ol> 
	   
